package plus.extvos.logging.annotation.type;

/**
 * @author Mingcai SHEN
 * @apiNote: 日志类型
 */

public enum LogAction {
    /**
     * 增删改查
     */
    CREATE("CREATE"),
    SELECT("SELECT"),
    UPDATE("UPDATE"),
    DELETE("DELETE");

    private String value;

    LogAction(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
